// Função de multiplicação
function multiplicacao(num1: number, num2: number): number {
    return num1 * num2;
}

// Função de saudação
function saudacao(nome: string): string {
    return `Olá ${nome}`;
}

// Testando as funções
console.log(multiplicacao(5, 3)); 
console.log(saudacao("João")); 
